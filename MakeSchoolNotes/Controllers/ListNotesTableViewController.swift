import UIKit
import RealmSwift

class ListNotesTableViewController: UITableViewController {
   
    //This is calling on results from notes in Realm and reloading the Table View to show the data implemented(showing the notes).
    var notes: Results<Note>! { didSet { tableView.reloadData() } }
    
    override func tableView(tableView: UITableView, numberOfRowsInSection section: Int) -> Int { return notes.count }
    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("listNotesTableViewCell", forIndexPath: indexPath) as! ListNotesTableViewCell
        
        // index path.row is 0 first time it's called, 1 the second time, etc...(it's iterating through)
        let row = indexPath.row
        
        // 2
        let note = notes[row]
        
        // 3
        cell.noteTitleLabel.text = note.title
        
        // 4
        cell.noteModificationTimeLabel.text = note.modificationTime.convertToString()
        
        return cell
    }
       
